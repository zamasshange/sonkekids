const flat = [];
for (const category of CATALOG.categories) {
  for (const title of category.games) {
    flat.push({
      id: title
        .toLowerCase()
        .replace(/[^a-z0-9]+/g, "-")
        .replace(/^-|-$/g, ""),
      title,
      categoryId: category.id,
      category: category.title,
    });
  }
}

const output = {
  version: 1,
  totalGames: flat.length,
  totalCategories: CATALOG.categories.length,
  categories: CATALOG.categories.map((category) => ({
    id: category.id,
    title: category.title,
    count: category.games.length,
    games: category.games.map((title) => ({
      id: title
        .toLowerCase()
        .replace(/[^a-z0-9]+/g, "-")
        .replace(/^-|-$/g, ""),
      title,
    })),
  })),
  games: flat,
};

writeFileSync(
  join(root, "content", "sonke-games-catalog.json"),
  `${JSON.stringify(output, null, 2)}\n`,
  "utf8",
);

console.log(
  `Wrote ${output.totalGames} games in ${output.totalCategories} categories.`,
);
